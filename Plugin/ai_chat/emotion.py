# 文本识别表情内容
def emotion_recognition(response):
    """
    基于关键词，简单的情绪识别
    :param response: 需要情绪识别的文本
    :return: 表情内容
    """

    # =========== 开心 ==============
    happy = [
        "笑",
        "不错",
        "哈",
        "开心",
        "呵",
        "嘻",
        "你好",
        "在吗",
        "干嘛",
        "名字",
        "欢迎",
        "早安",
        "晚安",
    ]

    # =========== 生气 ==============
    angry = ["生气", "不理你", "骂", "臭", "打死", "可恶", "白痴", "忘记"]

    # =========== 尴尬 ==============
    awkward = ["尴尬", "无聊", "无奈", "傻子", "郁闷", "笨蛋"]

    # =========== 厌恶 ==============
    disgust = ["恶心"]

    # =========== 疑问 ==============
    question = ["疑问", "？"]

    emotions = ["happy", "angry", "awkward", "question", "disgust"]

    for i in emotions:
        for j in eval(i):
            if j in response:
                return i

    return "neutral"


